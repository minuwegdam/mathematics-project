<?php
// This is an example PHP script demonstrating how to use arrays and loops.
// In this script, we create an array of numbers from 0 to 9 and then loop through it.

$numbers = range(0, 9);

foreach ($numbers as $number) {
    echo "Number: " . $number . "<br>";
}

